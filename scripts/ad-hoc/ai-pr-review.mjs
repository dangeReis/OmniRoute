import { execSync } from "node:child_process";
import fs from "node:fs";

async function getDiff() {
  const baseBranch = process.env.GITHUB_BASE_REF || "main";
  try {
    // Ensure we have fetched the base branch
    execSync(`git fetch origin ${baseBranch}`, { stdio: "ignore" });
    const diff = execSync(`git diff origin/${baseBranch}...HEAD`, { encoding: "utf8" });
    return diff;
  } catch (err) {
    console.warn("Could not get git diff using origin. Trying HEAD~1...", err.message);
    try {
      return execSync("git diff HEAD~1", { encoding: "utf8" });
    } catch (fallbackErr) {
      console.error("Failed to get git diff:", fallbackErr.message);
      return "";
    }
  }
}

async function callGemini(diff, apiKey) {
  const url = `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key=${apiKey}`;
  const prompt = `You are a senior software engineering assistant. Please perform a detailed code review on the following git diff. Identify bugs, security issues, performance problems, and styling anomalies. Keep your recommendations concise and actionable.

Diff:
\`\`\`diff
${diff}
\`\`\`
`;

  try {
    const response = await fetch(url, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        contents: [{ parts: [{ text: prompt }] }]
      })
    });
    if (!response.ok) {
      const errText = await response.text();
      return `**Gemini Review Error**: API responded with status ${response.status}: ${errText}`;
    }
    const data = await response.json();
    return data.candidates?.[0]?.content?.parts?.[0]?.text || "No review content generated by Gemini.";
  } catch (err) {
    return `**Gemini Review Error**: ${err.message}`;
  }
}

async function callClaude(diff, apiKey) {
  const url = "https://api.anthropic.com/v1/messages";
  const prompt = `You are a senior software engineering assistant. Please perform a detailed code review on the following git diff. Identify bugs, security issues, performance problems, and styling anomalies. Keep your recommendations concise and actionable.

Diff:
\`\`\`diff
${diff}
\`\`\`
`;

  try {
    const response = await fetch(url, {
      method: "POST",
      headers: {
        "x-api-key": apiKey,
        "anthropic-version": "2023-06-01",
        "content-type": "application/json"
      },
      body: JSON.stringify({
        model: "claude-3-5-sonnet-20241022",
        max_tokens: 2048,
        messages: [{ role: "user", content: prompt }]
      })
    });
    if (!response.ok) {
      const errText = await response.text();
      return `**Claude Review Error**: API responded with status ${response.status}: ${errText}`;
    }
    const data = await response.json();
    return data.content?.[0]?.text || "No review content generated by Claude.";
  } catch (err) {
    return `**Claude Review Error**: ${err.message}`;
  }
}

async function callOpenAI(diff, apiKey) {
  const url = "https://api.openai.com/v1/chat/completions";
  const prompt = `You are a senior software engineering assistant. Please perform a detailed code review on the following git diff. Identify bugs, security issues, performance problems, and styling anomalies. Keep your recommendations concise and actionable.

Diff:
\`\`\`diff
${diff}
\`\`\`
`;

  try {
    const response = await fetch(url, {
      method: "POST",
      headers: {
        "Authorization": `Bearer ${apiKey}`,
        "Content-Type": "application/json"
      },
      body: JSON.stringify({
        model: "gpt-4o-mini",
        messages: [{ role: "user", content: prompt }]
      })
    });
    if (!response.ok) {
      const errText = await response.text();
      return `**OpenAI/Codex Review Error**: API responded with status ${response.status}: ${errText}`;
    }
    const data = await response.json();
    return data.choices?.[0]?.message?.content || "No review content generated by OpenAI/Codex.";
  } catch (err) {
    return `**OpenAI/Codex Review Error**: ${err.message}`;
  }
}

async function run() {
  const diff = await getDiff();
  if (!diff) {
    console.log("No diff found. Skipping review.");
    return;
  }

  // Cap diff size at 40KB to prevent context limit errors on lightweight models
  let processedDiff = diff;
  if (diff.length > 40000) {
    processedDiff = diff.slice(0, 40000) + "\n\n... [Diff truncated due to size limit] ...";
  }

  let reviewMarkdown = "# 🤖 Automated AI Code Review\n\n";
  let hasReview = false;

  const geminiKey = process.env.GEMINI_API_KEY;
  const claudeKey = process.env.ANTHROPIC_API_KEY;
  const openaiKey = process.env.OPENAI_API_KEY;

  if (geminiKey) {
    console.log("Running Gemini Code Review...");
    const review = await callGemini(processedDiff, geminiKey);
    reviewMarkdown += `## ♊ Gemini Code Review\n\n${review}\n\n`;
    hasReview = true;
  }

  if (claudeKey) {
    console.log("Running Claude Code Review...");
    const review = await callClaude(processedDiff, claudeKey);
    reviewMarkdown += `## 💬 Claude Code Review\n\n${review}\n\n`;
    hasReview = true;
  }

  if (openaiKey) {
    console.log("Running OpenAI/Codex Code Review...");
    const review = await callOpenAI(processedDiff, openaiKey);
    reviewMarkdown += `## 🧠 OpenAI/Codex Review\n\n${review}\n\n`;
    hasReview = true;
  }

  if (!hasReview) {
    console.log("No API keys (GEMINI_API_KEY, ANTHROPIC_API_KEY, OPENAI_API_KEY) found. Skipping automated reviews.");
    return;
  }

  // Output review comment
  fs.writeFileSync("ai_review_comment.md", reviewMarkdown);
  console.log("Review completed! Comment saved to ai_review_comment.md");

  // Post comment to PR if GITHUB_TOKEN and GITHUB_EVENT_PATH are present
  const prNumber = process.env.GITHUB_EVENT_NAME === "pull_request" ? process.env.GITHUB_PR_NUMBER : null;
  if (process.env.GITHUB_TOKEN && prNumber) {
    try {
      execSync(`gh pr comment ${prNumber} -F ai_review_comment.md`, { stdio: "inherit" });
      console.log(`Review posted successfully to PR #${prNumber}`);
    } catch (postErr) {
      console.error("Failed to post comment to PR via GitHub CLI:", postErr.message);
    }
  } else {
    console.log("Not in a GitHub Actions PR environment or GITHUB_TOKEN is missing. Skipping posting comment.");
  }
}

run();
